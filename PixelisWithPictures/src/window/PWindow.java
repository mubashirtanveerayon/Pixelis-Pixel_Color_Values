    public void update(int x,int y){
        this.x=x;
        this.y=y;
        Color rgb=null;
        try{
            rgb = new Robot().getPixelColor(this.x, this.y);
        }catch(Exception ex){
            System.out.println(ex);
        };
        red = rgb.getRed();
        green = rgb.getGreen();
        blue = rgb.getBlue();
    }
    
